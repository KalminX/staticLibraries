#include <stdio.h>
#include "math_functions.h"
/**
* add - add two numbers
* @a: the first number
* @b: the second number
* Return: return the results for each function
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* subtract - subtract two numbers
* @a: the first number
* @b: the second number
* Return: returns the difference
*/
int subtract(int a, int b)
{
	return (a - b);
}
/**
* multiply - multiply two numbers
* @a: the first number
* @b: the second number
* Return: returns the product
*/
int multiply(int a, int b)
{
	return (a * b);
}
/**
* divide - divide the first number by the second number
* @a: the first number
* @b: the second number
* Return: returns the quotient
*/
int divide(int a, int b)
{
	return (a / b);
}
